# Arithmetic Operations in Python

# Operators

print("Adition", 5+3)
print("Subtraction", 5-3)
print("Multiplication", 5*3)
print("Division", 5/3)
print("Division without the modulus", 5//2)
print("Modulus", 5%3)
print("Exponentiation", 5**3)
print("Mixed: ", 5*3-5/2+2)

# Complex Numbers
print("Complex number: ", 1+1j)

# Comparison Operators

print(3 < 5)
print(3 > 5)
print(3 == 5)
print(3 != 5)
print(3 <= 5)
print(3 >= 5)

print("Hi" < "Python")
print("Hi" > "Python")
print("aaa" == "aaa")
print("Hi" != "Python")
print("Hi" <= "Python")
print("Hi" >= "Python")

# Logic operators

print(3 < 4 and "Hi" < "Python")
print(3 < 4 or "Hi" < "Python")
print(not(3 < 4))

# Calculating the area of a circle
