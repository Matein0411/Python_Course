ages = [19, 22, 24, 20, 25, 26, 24, 25, 24]

# sort the list and find the min and max age
print(ages.sort())
min_age = min(ages)
print(f"Min age of the list is: {min_age}")
max_age = max(ages)
print(f"Max age of the list is: {max_age}")

# add the min age and the max age again to the list
ages.append(min_age)
ages.insert(ages[-1], max_age)
print(ages)

# find the median age (one middle item or two middle items divided by two)
ages.sort()
ages.append(45)
print(ages)

if len(ages) % 2 == 0:
    mid = len(ages) // 2
    median_age = (ages[mid -1] + ages[mid]) /2
    print(f"The median age of the list is {median_age}")
else:
    mid = len(ages) //2
    median_age = ages[mid]
    print(f"The median age of the list is {median_age}")

# find the average age (sum of all items)

avg = sum(ages) / len(ages)

# compare the value of (min - avg) and (max - avg) 
print(abs(min_age - avg))
print(abs(max_age - avg))

